fun main() {

    // Liest den Namen ein, und begrüßt den Nutzer
    println("Hallo, wie heißt du? (confirm with Enter)")
    var name = readln()
    println("Hallo $name!")

    // Druckt eine leere Zeile aus
    println()

    // Liest das Alter ein
    println("Wie alt bist du? (confirm with Enter)")
    var alter = readln()    // Diese Zeile liest das Alter ein
    println("Wow! Bereits $alter Jahre?!")
}